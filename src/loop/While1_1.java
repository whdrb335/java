package loop;

public class While1_1 {
    public static void main(String[] args) {
        int count = 0;

        count = count + 1;
        System.out.println("현재 숫자는 :" + count);
        count = count + 1;
        System.out.println("현재 숫자는 :" + count);
        count = count + 1;
        System.out.println("현재 숫자는 :" + count);

        // 여기서 만약이것을 100번 반복한다면? 그럼 100번을 적어야 하기 때문에 힘들어!!
    }
}
