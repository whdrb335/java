package java_start.method.ex;

public class MethodEx2 {
    public static void main(String[] args) {

        num(3);
        System.out.println();
        num(7);



    }
    public static void num(int times) {
        for (int i=0; i<times; i++) {
            System.out.println("Hello, world!");

        }

    }


}
